// === - equality operator
// !== - inequality operator
// < - less than operator
// > - greater than operator
// <= - less than or equal to operator
// >= - greater than or equal to operator

let temp = 80

if (temp <= 32) {
    console.log('It is freezing outside!')
}
else if (temp >= 110) {
    console.log('It is way too hot outside!')
}
else {
    console.log('The temperature is okay outside.')
}

